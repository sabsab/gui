    public void setFilterEffect(int effectNum)
    {
        
        if(effectsPanel.footageProperties != null)
        {
            filterEffect = effectsPanel.footageProperties.footage.core.getFilterEffect(effectNum);
        }
        else if(effectsPanel.channelProperties != null)
        {
            filterEffect = effectsPanel.channelProperties.channel.core.getFilterEffect(effectNum);
        }
        else if(effectsPanel.coreProperties != null)
        {
            filterEffect = effectsPanel.coreProperties.core.getFilterEffect(effectNum);
        }
        
        
        
        jLabel1.setText(filterEffect.getDescription());
        
        
        if(effectNum == 0)
        {
            rgba = new videoMixerLite.resources.effects.RGBA();
            rgba.init(this, filterEffect);
            jScrollPane1.setViewportView(rgba);
        }
        else if(effectNum == 1)
        {
            greyScale = new videoMixerLite.resources.effects.GreyScale();
            greyScale.init(this, filterEffect);
            jScrollPane1.setViewportView(greyScale);
        }
        
        
    }
    
    
    public GLTextureFilter getFilterEffect()
    {
        if(rgba != null)
        {
            rgba.setParameters();
        }
        else if(greyScale != null)
        {
            greyScale.setParameters();
        }
        
        return filterEffect;
    }
    
    

